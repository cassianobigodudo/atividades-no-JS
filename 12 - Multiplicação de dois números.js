/*Faça um programa no qual o usuário digite dois números e mostre na tela a multiplicação desses números. */

let numeroUm, numeroDois, resultadoNumeros

numeroUm = Number(prompt('Digite seu número'))
numeroDois = Number(prompt('Digite seu outro número'))

resultadoNumeros = numeroUm*numeroDois

alert(`Ao multiplicar ${numeroUm} e ${numeroDois} o resultado dá ${resultadoNumeros}`)
